# 1> DOCSTRING IN PYTHON- a docstring is a string literal that occurs as the first statement in a module, function, class, or method definition. Such a docstring becomes the __doc__ special attribute of that object.
#it helps in providing meaningful information for the code,making it easier to understand and maintain.
#key points:
#1> docstring is written in triple quotes.
#2> docstring is written in the beginning of the module, function, class, or method.
#3> docstring is used to provide information about the object.
# it describes what the unctionclas/module does, its parameters,and return values.
# example:
# def add(a,b):
#     '''this function adds two numbers'''
#     return a+b
